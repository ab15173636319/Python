# 大写（只针对字母）
# upper()
str1 = "abc"
print(f"大写后的{str1}变成{str1.upper()}")

# ----------------------------------------------------------------

# 小写
# lower()
str2 = "ABC"
print(f"小写后的{str2}变成{str2.lower()}")

# ----------------------------------------------------------------

# 将字母首字母大写其余小写
# capitalize

str3 = "abcde"
str4 = "ABCDE"

print(f"{str3}变成{str3.capitalize()}")
print(f"{str4}变成{str4.capitalize()}")

# ----------------------------------------------------------------

# 将字符串中每个单词首字母大写，其余小写
# title()

str5 = "go for it!"
str6 = "aBc d哈哈哈f great"

print(f"{str5}变成{str5.title()}")
print(f"{str6}变成{str6.title()}")

# ----------------------------------------------------------------

# 将字符串中字母大小写互换，即大写转小写，小写转大写
# swapcase()

str7 = "AbCdEfGh哈哈哈IjK"
print(f"{str7}变成{str7.swapcase()}")
