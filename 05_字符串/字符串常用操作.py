# 一系列字符串方法

# ----------------------------------------------------------------

# 分割、合并字符串
# split(sep,maxsplit)


str11 = "合抱之木，生于毫末；九层之台，起于累土；千里之行，始于足下。"
# 按照，分割字符串
print(str11.split("，"))
# 最多分割一次
print(str11.split("，", 1))

# ----------------------------------------------------------------

join_str = ["合抱之木", "生于毫末；九层之台", "起于累土；千里之行", "始于足下。"]
print(",".join(join_str))

# ----------------------------------------------------------------
# 截取字符串
# string[start:end:step] 如列表一样，索引是从0开始的
str10 = "abcdefg"
print(f"截取第三个字符：{str10[2]}")
print(f"截取前两个字符：{str10[0:2]}")
print(f"截取所有字符，但隔一取一：{str10[0:len(str10):2]}")

# ----------------------------------------------------------------

# 编码
# encode()

str9 = "你好"

# 将字符串变量 str9 转换为 UTF-8 编码的字节序列
str_encode = str9.encode("utf-8")
print(str_encode)
# 解码回字符串
print(str_encode.decode("utf-8"))

# ----------------------------------------------------------------

# 查找与替换
# find(find_value)
# 返回查找内容在字符串中首次出现时的索引，不存在返回-1

str8 = "路曼曼其修远兮，吾将上下而求索。"

print(f"’哈哈哈’是否在’{str8}’中？{'在' if str8.find('哈哈哈') != -1 else '不在'}")

